//Operator on bitsand perform bit by bit operator
/*
1. AND & ---> true if both operand are true. else, false.
2. OR | ----> true if one of them is true
3. XOR ^ -----> true f both operand are same 00 or 11.
4. Ones Complement ~ ----> 0 converts to 1 and 1 converts to 0.
5. left shift operator << -----> shift the operator in left direction as per tha value of right operand.
eg:--- 4 << 1
       (0100)
       =1000
6. Right Shift Operator >> -----> shift the operator in right direction as per the value of the left operand.
eg:----4 >> 1
       (0100)
       =0010

formula for left shift operator =====>  a << n ====== a * 2^n
formula for Right Shift operator =====> a >> n ====== a / 2^n
       */